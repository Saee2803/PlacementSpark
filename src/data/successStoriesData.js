/**
 * Get stories filtered by company
 */
export const getStoriesByCompany = (company) => {
  if (company === "all") return successStoriesData;
  if (company === "other") {
    const mainCompanies = ["Reliance", "Lubrizol", "TCS"];
    return successStoriesData.filter(story => !mainCompanies.includes(story.company));
  }
  return successStoriesData.filter(story => story.company === company);
};

/**
 * Get all unique companies from stories
 */
export const getUniqueCompanies = () => {
  return [...new Set(successStoriesData.map(story => story.company))];
};
